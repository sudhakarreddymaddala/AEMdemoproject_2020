public class CategoryPojo {

	private String catName;
	private String catThumbnail;
	private String catAlt;
	private String catUrl;
	private String catTarget;
	private String analyticsCategoryName;

	public String getCatName() {
		return catName;
	}

	public void setCatName(String catName) {
		this.catName = catName;
	}

	public String getCatThumbnail() {
		return catThumbnail;
	}

	public void setCatThumbnail(String catThumbnail) {
		this.catThumbnail = catThumbnail;
	}

	public String getCatAlt() {
		return catAlt;
	}

	public void setCatAlt(String catAlt) {
		this.catAlt = catAlt;
	}

	public String getCatUrl() {
		return catUrl;
	}

	public void setCatUrl(String catUrl) {
		this.catUrl = catUrl;
	}

	public String getCatTarget() {
		return catTarget;
	}

	public void setCatTarget(String catTarget) {
		this.catTarget = catTarget;
	}

	public String getAnalyticsCategoryName() {
		return analyticsCategoryName;
	}

	public void setAnalyticsCategoryName(String analyticsCategoryName) {
		this.analyticsCategoryName = analyticsCategoryName;
	}
}
