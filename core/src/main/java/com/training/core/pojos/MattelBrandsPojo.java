public class MattelBrandsPojo {

	
	private String logoImage;
	private String logoAltText;
	private String logoURL;
	private String targetURL;
	private String alwaysEnglish;
	
	public String getLogoImage() {
		return logoImage;
	}
	public void setLogoImage(String logoImage) {
		this.logoImage = logoImage;
	}
	public String getLogoAltText() {
		return logoAltText;
	}
	public void setLogoAltText(String logoAltText) {
		this.logoAltText = logoAltText;
	}
	public String getLogoURL() {
		return logoURL;
	}
	public void setLogoURL(String logoURL) {
		this.logoURL = logoURL;
	}
	public String getTargetURL() {
		return targetURL;
	}
	public void setTargetURL(String targetURL) {
		this.targetURL = targetURL;
	}
	public String getAlwaysEnglish() {
		return alwaysEnglish;
	}
	public void setAlwaysEnglish(String alwaysEnglish) {
		this.alwaysEnglish = alwaysEnglish;
	}

	
	
}
