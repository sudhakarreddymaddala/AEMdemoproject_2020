public class SocialIconsPojo {
	private String icons;
	private String linkText;
	private String socialLinkURL;
	private String linkTarget;
	private String alwaysEnglish; 
	public String getIcons() {
		return icons;
	}
	public void setIcons(String icons) {
		this.icons = icons;
	}
	public String getLinkText() {
		return linkText;
	}
	public void setLinkText(String linkText) {
		this.linkText = linkText;
	}
	
	public String getLinkTarget() {
		return linkTarget;
	}
	public void setLinkTarget(String linkTarget) {
		this.linkTarget = linkTarget;
	}
	public String getAlwaysEnglish() {
		return alwaysEnglish;
	}
	public void setAlwaysEnglish(String alwaysEnglish) {
		this.alwaysEnglish = alwaysEnglish;
	}
	public String getSocialLinkURL() {
		return socialLinkURL;
	}
	public void setSocialLinkURL(String socialLinkURL) {
		this.socialLinkURL = socialLinkURL;
	}
	

}
