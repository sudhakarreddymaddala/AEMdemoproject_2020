package com.training.core.pojos;

public class PlaySitePojo {
	private String siteLabel;
	private String siteLink;
	private String siteTarget;

	public String getSiteLabel() {
		return siteLabel;
	}

	public void setSiteLabel(String siteLabel) {
		this.siteLabel = siteLabel;
	}

	public String getSiteLink() {
		return siteLink;
	}

	public void setSiteLink(String siteLink) {
		this.siteLink = siteLink;
	}

	public String getSiteTarget() {
		return siteTarget;
	}

	public void setSiteTarget(String siteTarget) {
		this.siteTarget = siteTarget;
	}

	
}
